public class d_5_4 {
    public static void main(String[] args) {
        double x = Math.rint(40.6);
        double y = Math.abs(40.6);
        System.out.println("Value of X = " + x + " and Y is = " + y);
        double a = Math.floor(40.6);
        double b = Math.ceil(40.6);
        double c = Math.round(40.6);
        System.out.println("Value of A = " + a + " and B is = " + b + " and C is = " + c);
    }
}
