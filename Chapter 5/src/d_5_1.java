public class d_5_1 {
    public static void main(String[] args) {
        int i = 42 + 45 - 48 - 5;
        int j = 5 + 5 - 8 + 2;
        System.out.println("i = " + i);
        System.out.println("j = " + j);
        int ans = i % j;
        System.out.println("Value of Ans: i % j = " + ans);
        ans = i / j;
        System.out.println("Value of Ans: i / j = " + ans);
    }
}
