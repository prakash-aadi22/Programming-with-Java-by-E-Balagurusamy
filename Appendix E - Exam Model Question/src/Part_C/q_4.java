package Part_C;

public class q_4 {
    public static void main(String[] args) {
        int x = 0, n1, n2, n3, n4;
        switch (x) {
            case 1:
                n1 = 10;
                n2 = 20;
                System.out.println("n1 = " + n1 + ", n2 = " + n2);
            case 2:
                n3 = 30;
                System.out.println("n3 = " + n3);
                break;
            default:
                n4 = 40;
                System.out.println("n4 = " + n4);
        }
    }
}
